// ========== EXAM ENGINE ==========
let activeQuestions = [];
let currentQIdx = 0;
let score = 0;
let userAnswers = [];
let timerInterval;
let totalSeconds = 60 * 60; // 60 minutes

function shuffleArray(arr) {
    for (let i = arr.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
}

function startExam() {
    const sections = ["Problem Solving", "Teamwork & Communication", "Ethics & Professionalism", "Creativity & Adaptability"];
    activeQuestions = [];

    sections.forEach(sec => {
        let secQs = rawQuestions.filter(q => q.section === sec);
        secQs = shuffleArray([...secQs]);
        // Limit to matching distribution if needed, but let's take all questions (which is 15 * 4 = 60 questions)
        secQs.forEach(q => {
            const optionIndices = q.options.map((_, i) => i);
            const shuffledIndices = shuffleArray([...optionIndices]);
            const newOptions = shuffledIndices.map(i => q.options[i]);
            const newAnswer = shuffledIndices.indexOf(q.answer);
            activeQuestions.push({
                ...q,
                options: newOptions,
                answer: newAnswer
            });
        });
    });

    score = 0;
    currentQIdx = 0;
    userAnswers = new Array(activeQuestions.length).fill(null);
    totalSeconds = 60 * 60;

    document.getElementById('start-screen').style.display = 'none';
    document.getElementById('quiz-screen').style.display = 'block';

    buildQuestionMatrix();
    loadQuestion();
    startTimer();
}

function buildQuestionMatrix() {
    const matrix = document.getElementById('q-matrix');
    matrix.innerHTML = '';
    for (let i = 0; i < activeQuestions.length; i++) {
        const cell = document.createElement('div');
        cell.className = 'q-badge';
        cell.innerText = i + 1;
        cell.onclick = () => jumpToQuestion(i);
        matrix.appendChild(cell);
    }
}

function updateQuestionMatrixUI() {
    const cells = document.querySelectorAll('.q-badge');
    cells.forEach((cell, idx) => {
        cell.className = 'q-badge';
        if (idx === currentQIdx) cell.classList.add('current');
        if (userAnswers[idx] !== null) {
            cell.classList.add(userAnswers[idx].isCorrect ? 'answered-correct' : 'answered-incorrect');
        }
    });
}

function startTimer() {
    const timerEl = document.getElementById('timer');
    timerInterval = setInterval(() => {
        totalSeconds--;
        if (totalSeconds <= 0) {
            clearInterval(timerInterval);
            finishExam();
            return;
        }
        const m = Math.floor(totalSeconds / 60);
        const s = totalSeconds % 60;
        timerEl.innerText = `${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}`;

        if (totalSeconds <= 300) {
            timerEl.style.color = 'var(--danger)';
            timerEl.style.animation = 'pulse 1s infinite';
        } else if (totalSeconds <= 600) {
            timerEl.style.color = 'var(--warning)';
        }
    }, 1000);
}

function loadQuestion() {
    const q = activeQuestions[currentQIdx];

    document.getElementById('section-title').innerText = q.partTitle;
    document.getElementById('question-index-label').innerText = `Question ${currentQIdx + 1} of ${activeQuestions.length}`;
    document.getElementById('time-suggest').innerText = `Suggested Time: ${q.suggestedTime}`;

    const progress = ((currentQIdx + 1) / activeQuestions.length) * 100;
    document.getElementById('progress-bar').style.width = progress + '%';

    const layout = document.getElementById('dynamic-question-layout');
    let html = '';

    if (q.passage) {
        html += `<div class="passage-layout"><div class="passage-box">${q.passage}</div><div>`;
    }

    html += `<div class="question-text">${q.text}</div>`;
    html += `<div class="options-container">`;
    q.options.forEach((opt, idx) => {
        const isAnswered = userAnswers[currentQIdx] !== null;
        let cls = 'option-btn';
        if (isAnswered) {
            if (idx === q.answer) cls += ' correct';
            else if (userAnswers[currentQIdx] && userAnswers[currentQIdx].selected === idx) cls += ' wrong';
            cls += ' disabled';
        }
        html += `<button class="${cls}" onclick="selectOption(${idx})" ${isAnswered ? 'disabled' : ''}>${String.fromCharCode(65 + idx)}. ${opt}</button>`;
    });
    html += `</div>`;

    if (q.passage) html += `</div></div>`;

    layout.innerHTML = html;

    const submitBtn = document.getElementById('submit-ans-btn');
    const nextBtn = document.getElementById('next-btn');
    const explPanel = document.getElementById('explanation-panel');

    if (userAnswers[currentQIdx] !== null) {
        submitBtn.style.display = 'none';
        nextBtn.style.display = 'inline-flex';
        document.getElementById('explanation-correct').innerHTML = q.correctExplanation;
        document.getElementById('explanation-wrong').innerHTML = q.wrongExplanation;
        document.getElementById('explanation-mindset').innerHTML = q.mindset;
        document.getElementById('explanation-hack').innerHTML = q.speedHack;
        explPanel.style.display = 'flex';
    } else {
        submitBtn.style.display = 'inline-flex';
        submitBtn.disabled = true;
        nextBtn.style.display = 'none';
        explPanel.style.display = 'none';
    }

    updateQuestionMatrixUI();
    window._selectedOption = null;
}

window._selectedOption = null;

function selectOption(idx) {
    if (userAnswers[currentQIdx] !== null) return;
    window._selectedOption = idx;

    document.querySelectorAll('.option-btn').forEach((btn, i) => {
        btn.classList.remove('selected');
        if (i === idx) btn.classList.add('selected');
    });

    document.getElementById('submit-ans-btn').disabled = false;
}

function submitAnswer() {
    if (window._selectedOption === null) return;

    const q = activeQuestions[currentQIdx];
    const isCorrect = window._selectedOption === q.answer;
    if (isCorrect) score++;

    userAnswers[currentQIdx] = {
        selected: window._selectedOption,
        isCorrect: isCorrect
    };

    document.querySelectorAll('.option-btn').forEach((btn, idx) => {
        btn.disabled = true;
        btn.classList.add('disabled');
        if (idx === q.answer) btn.classList.add('correct');
        else if (idx === window._selectedOption && !isCorrect) btn.classList.add('wrong');
    });

    document.getElementById('submit-ans-btn').style.display = 'none';
    document.getElementById('next-btn').style.display = 'inline-flex';

    document.getElementById('explanation-correct').innerHTML = q.correctExplanation;
    document.getElementById('explanation-wrong').innerHTML = q.wrongExplanation;
    document.getElementById('explanation-mindset').innerHTML = q.mindset;
    document.getElementById('explanation-hack').innerHTML = q.speedHack;

    document.getElementById('explanation-panel').style.display = 'flex';
    updateQuestionMatrixUI();
}

function nextQuestion() {
    if (currentQIdx < activeQuestions.length - 1) {
        currentQIdx++;
        loadQuestion();
    } else {
        finishExam();
    }
}

function jumpToQuestion(idx) {
    currentQIdx = idx;
    loadQuestion();
}

function finishExam() {
    clearInterval(timerInterval);
    document.getElementById('quiz-screen').style.display = 'none';
    document.getElementById('result-screen').style.display = 'block';

    const percent = ((score / activeQuestions.length) * 100).toFixed(1);
    document.getElementById('final-score').innerText = score;

    const readiness = document.getElementById('readiness-banner');

    if (percent >= 85) {
        readiness.innerText = `LEVEL A+: PHARMACY FUTURE LEADER (${percent}%)`;
        readiness.className = "readiness-banner ready-high";
    } else if (percent >= 70) {
        readiness.innerText = `LEVEL B: COMPETITIVE PROFESSIONAL (${percent}%)`;
        readiness.className = "readiness-banner ready-mid";
    } else {
        readiness.innerText = `LEVEL C: NEEDS MINDSET POLISHING (${percent}%)`;
        readiness.className = "readiness-banner ready-low";
    }

    const sections = ["Problem Solving", "Teamwork & Communication", "Ethics & Professionalism", "Creativity & Adaptability"];
    const sectionalScores = {};
    sections.forEach(sec => {
        sectionalScores[sec] = { total: 0, correct: 0 };
    });

    activeQuestions.forEach((q, idx) => {
        sectionalScores[q.section].total++;
        if (userAnswers[idx] && userAnswers[idx].isCorrect) {
            sectionalScores[q.section].correct++;
        }
    });

    const chartContainer = document.getElementById('sectional-charts-container');
    chartContainer.innerHTML = '';
    let weakestSection = "";
    let lowestPercent = 101;

    sections.forEach(sec => {
        const data = sectionalScores[sec];
        const secPercent = data.total > 0 ? ((data.correct / data.total) * 100).toFixed(0) : 0;
        if (Number(secPercent) < lowestPercent) {
            lowestPercent = Number(secPercent);
            weakestSection = sec;
        }

        const chartRow = document.createElement('div');
        chartRow.className = 'chart-row';
        chartRow.innerHTML = `
            <div class="chart-labels"><span>${sec}</span><span>${data.correct}/${data.total} (${secPercent}%)</span></div>
            <div class="chart-bar-bg"><div class="chart-bar-fill" style="width: ${secPercent}%"></div></div>`;
        chartContainer.appendChild(chartRow);
    });

    const weaknessBox = document.getElementById('weakness-analysis');
    let weaknessHTML = "";

    if (percent >= 85) {
        weaknessHTML += `
            <p>🏆 <b>ทัศนคติวิชาชีพของท่านโดดเด่นมาก!</b> ท่านสะท้อน Mindset ของผู้นำในวิชาชีพสาธารณสุขยุคใหม่ที่เพียบพร้อมด้วยจริยธรรม ทักษะการวิเคราะห์วิกฤต และ Empathy</p>
        `;
    } else {
        weaknessHTML += `
            <p>🩺 <b>พาร์ตที่ต้องเน้นการปรับ Mindset:</b> <b style="color:var(--danger)">[${weakestSection}]</b> (${lowestPercent}%)</p>
        `;
    }

    weaknessHTML += `<h4>🎯 คำแนะนำทัศนคติวิชาชีพเฉพาะตัว:</h4>`;

    const tips = {
        "Problem Solving": "ฝึกกระบวนการคัดกรองอุบัติการณ์ (Incident reports) และ Medication safety protocols เน้นความปลอดภัยคนไข้เชิงรุก 100% เสมอ",
        "Teamwork & Communication": "เน้นการเจรจาสหวิชาชีพ (Interprofessional cooperation) สื่อสารกับแพทย์อย่างมืออาชีพด้วยหลักวิชาการ และการควบคุมความเครียดในเหตุการณ์โวยวายของคนไข้",
        "Ethics & Professionalism": "ห้ามทำผิดกฎหมายยาควบคุมหรือความลับคนไข้เด็ดขาดไม่ว่าในสถานการณ์บีบคั้นใดๆ ให้ใช้กลไกการสงเคราะห์ที่เป็นระบบแทน",
        "Creativity & Adaptability": "เปิดใจต้อนรับปัญญาประดิษฐ์ (AI/Automation) พัฒนาทักษะเชิงลึก (Upskilling) และริเริ่มนวัตกรรมสุขภาพชุมชนที่ยั่งยืนและตรงตามสภาพชุมชนจริง"
    };

    sections.forEach(sec => {
        const isWeakest = sec === weakestSection ? `style="color: var(--danger); font-weight:700;"` : "";
        weaknessHTML += `<p><b ${isWeakest}>[${sec}]:</b> ${tips[sec]}</p>`;
    });

    weaknessBox.innerHTML = weaknessHTML;
}

function resetExam() {
    document.getElementById('result-screen').style.display = 'none';
    document.getElementById('start-screen').style.display = 'flex';
}
